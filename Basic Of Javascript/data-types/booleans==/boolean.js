// booleans
// true,false

let num1 = 5;
let num2 = 9;

console.log(num1 < num2);
console.log(num1 <= num2);

// ==,===

// == in this the js will only checks the value not the data type
let num3 = "5";
let num4 = 5;
console.log(num3 == num4);

// === in this the js will check the data type

console.log(num3 === num4);

// !=,!==
console.log(num3 != num4);
// in this js will check the number js will first convert into 1 type and then compare it , the string is converted into number

console.log(num3 !== num4);
// in this the type is also check
